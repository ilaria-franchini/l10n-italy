def cee_group_mapping_init(cr, registry):
    env = api.Environment(cr, SUPERUSER_ID, {})
    for record in CEE_ACCOUNTS_MAPPING:
        accounts = env['account.account'].search([
            ('code', '=', record[0])], limit=1)
        accounts_group_cee = env['account.group'].search([
            ('code_prefix', '=', record[1]), ('cee_group', '=', True)], limit=1)
        if accounts and accounts_group_cee:
            accounts.cee_group_id = accounts_group_cee.id
    return True
